#!/usr/bin/env python3
"""
This module contains a single function to_str that returns the string representation of a float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.


    Args:
      n (float): the number to be converted to a string.


    Returns:
      str: The string representation of the number.
    """

    return str(n)
